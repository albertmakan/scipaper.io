package com.silvera.Library.service.impl;

import org.springframework.stereotype.Service;
import com.silvera.Library.domain.model.*;
import com.silvera.Library.service.base.*;
import com.silvera.Library.repository.*;
import org.springframework.beans.factory.annotation.Autowired;


@Service
public class LibraryService implements ILibraryService {

    @Autowired
    PublicationRepository publicationRepository;
  
    
    @Override
    public java.util.List<Publication> search(java.lang.String query){
        return publicationRepository.findAll();
    }
    

    @Override
    public void publishListener(com.silvera.Library.messages.publishmsggroup.PaperPublished message){
        Publication publication = new Publication();
        publication.setAuthor(message.getAuthor());
        publication.setPaperId(message.getPaperId());
        publication.setTitle(message.getTitle());
        publicationRepository.save(publication);
    }
}